class Evento {
  final String nombre;
  final String fecha;
  final String lugar;
  final String hora;
  final String descripcion;
  final String imagenUrl;

  Evento({
    required this.nombre,
    required this.fecha,
    required this.lugar,
    required this.hora,
    required this.descripcion,
    required this.imagenUrl,
  });
}
